package net.rudahee.metallics_arts.data.enums.interfaces;

/**
 * An interface representing a gem with methods to get gem names in different cases.
 *
 * @author SteelCode Team
 * @since 1.5.1
 */
public interface IGems {

    /**
     * Returns the lowercase gem name.
     *
     * @return the gem name in lowercase
     */
    String getGemNameLower();

    /**
     * Returns the uppercase gem name.
     *
     * @return the gem name in uppercase
     */
    String getGemNameUpper();
}
