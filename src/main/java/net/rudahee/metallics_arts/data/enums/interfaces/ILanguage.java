package net.rudahee.metallics_arts.data.enums.interfaces;

/**
 * An interface representing a language with methods to get names in different languages.
 *
 * @author SteelCode Team
 * @since 1.5.1
 */
public interface ILanguage {

    /**
     * Returns the name in Spanish.
     *
     * @return the name in Spanish
     */
    String getNameInSpanish();

    /**
     * Returns the name in English.
     *
     * @return the name in English
     */
    String getNameInEnglish();


}
